package beans;

public class Course 
{
	private String  cname;
	private String cfee;
	private String cid;
	
	
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getCfee() {
		return cfee;
	}
	public void setCfee(String cfee) {
		this.cfee = cfee;
	}
	public String getCid() {
		return cid;
	}
	public void setCid(String cid) {
		this.cid = cid;
	}
}
